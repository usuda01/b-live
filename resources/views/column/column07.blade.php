<p>ゲーム配信で儲かる収益化を狙う場合、注意しなければならないのが著作権です。</p>
<p>ゲームの場合、それを開発したメーカーや製作者が著作者となり、著作権法でその権利が保護されています。ゲーム配信前にゲームの利用規約などを確認しないと、アカウント停止などのペナルティのほか、法律で罰せられる可能性さえあります。</p>
<p>こちらではゲーム配信と著作権、収益化して稼ぐことができるゲームとできないゲームについて紹介します。</p>

<h3>ゲームと著作権について</h3>
<img class="image_area image_right" alt="ゲームと著作権について" src="https://live.carol-i.com/images/column/g_07_01.jpg">
<p>ゲーム配信する場合、著作権の侵害には気をつけましょう。本来、ゲームは著作権法において「映画の著作物」として扱われ、ゲームを製作した会社が著作者となります。</p>
<p>著作権法第21条「著作者は、その著作物を複製する権利を専有する（公衆送信権等）」、第23条「著作者は、その著作物について、公衆送信（自動公衆送信の場合にあっては、送信可能化を含む。）を行う権利を専有する」が、その根拠となっています。</p>
<p>わかりやすく説明すると、収益化していなくても、ゲーム実況でゲームの内容を視聴者に見せること、またゲーム配信そのものが著作権法的にNGということです。</p>
<p>しかし、だからといってすべてのゲーム配信が著作権法違反となるわけではありません。ゲームメーカーによっては、営利目的としないものならゲーム配信での使用を許可していたり、ゲーム実況の宣伝効果を認める形でその投稿や配信を許可しているところもあります。</p>
<p>そうしたものでなくても、正式に発表されているタイトルならゲーム配信可能としているメーカーもあり、一概に著作権法に照らし合わせてゲーム配信すべてが違反行為であるとはいえません。</p>
<p>そのため、ゲーム配信を行う前に、取り扱うゲームが配信に対してどのようなスタンスをとっているか確認しておく必要があります。ゲーム機やPCを使用したゲームはもちろん、スマホアプリのゲームも同様に確認しておきましょう。</p>
<p>「ほかの人がゲーム配信しているから大丈夫」という軽い気持ちで行わないよう、注意してください。</p>
<p>ゲーム配信で儲かるためには、まず配信可能なタイトルで行うことが大切です。そして、儲かるシステムがあるプラットフォームを活用して、ゲーム配信を始めましょう。</p>
<p>B-LIVEは、難しい審査もないため、すぐに活用できる配信プラットフォームです。PCから簡単に配信を始めることができます。ゲーム配信を行っている人、これから始めてみたい人は、ぜひチェックしてみてください。</p>

